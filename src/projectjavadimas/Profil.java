    private void tabelAnggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelAnggotaMouseClicked
        int baris = tabelAnggota.getSelectedRow();
        String pecah = tabelAnggota.getModel().getValueAt(baris, 5).toString();
        String[] lahir = pecah.split("-");
        String tanggal_lahir = lahir[2];
        String bln_lhr = lahir[1];
        String tahun_lahir = lahir[0];
        String bulan_lahir = null;
        if(bln_lhr.equalsIgnoreCase("01")){
            bulan_lahir = "Januari";
        }
        else if(bln_lhr.equalsIgnoreCase("02")){
            bulan_lahir = "Februari";
        }
        else if(bln_lhr.equalsIgnoreCase("03")){
            bulan_lahir = "Maret";
        }
        else if(bln_lhr.equalsIgnoreCase("04")){
            bulan_lahir = "April";
        }
        else if(bln_lhr.equalsIgnoreCase("05")){
            bulan_lahir = "Mei";
        }
        else if(bln_lhr.equalsIgnoreCase("06")){
            bulan_lahir = "Juni";
        }
        else if(bln_lhr.equalsIgnoreCase("07")){
            bulan_lahir = "Juli";
        }
        else if(bln_lhr.equalsIgnoreCase("08")){
            bulan_lahir = "Agustus";
        }
        else if(bln_lhr.equalsIgnoreCase("09")){
            bulan_lahir = "September";
        }
        else if(bln_lhr.equalsIgnoreCase("10")){
            bulan_lahir = "Oktober";
        }
        else if(bln_lhr.equalsIgnoreCase("11")){
            bulan_lahir = "November";
        }
        else if(bln_lhr.equalsIgnoreCase("12")){
            bulan_lahir = "Desember";
        }
        namdep.setText(tabelAnggota.getModel().getValueAt(baris, 2).toString());
        nambel.setText(tabelAnggota.getModel().getValueAt(baris, 3).toString());
        nohp.setText(tabelAnggota.getModel().getValueAt(baris, 4).toString());
        username.setText(tabelAnggota.getModel().getValueAt(baris, 0).toString());
        password.setText(tabelAnggota.getModel().getValueAt(baris, 1).toString());
        tgl.setSelectedItem(tanggal_lahir);
        bln.setSelectedItem(bulan_lahir);
        thn.setSelectedItem(tahun_lahir);
        String gender = tabelAnggota.getModel().getValueAt(baris, 6).toString();
        if(gender.equalsIgnoreCase("l")){
            laki.doClick();
        }
        else {
            cewe.doClick();
        }
    }//GEN-LAST:event_tabelAnggotaMouseClicked

