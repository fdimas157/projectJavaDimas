    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String tanggal, bul, tahun, jk;
        String bulan = null;
        tanggal = (String) tgl.getSelectedItem();
        bul = (String) bln.getSelectedItem();
        if(bul.equalsIgnoreCase("Januari")){
            bulan = "1";
        }
        else if(bul.equalsIgnoreCase("Februari")){
            bulan = "2";
        }
        else if(bul.equalsIgnoreCase("Maret")){
            bulan = "3";
        }
        else if(bul.equalsIgnoreCase("April")){
            bulan = "4";
        }
        else if(bul.equalsIgnoreCase("Mei")){
            bulan = "5";
        }
        else if(bul.equalsIgnoreCase("Juni")){
            bulan = "6";
        }
        else if(bul.equalsIgnoreCase("Juli")){
            bulan = "7";
        }
        else if(bul.equalsIgnoreCase("Agustus")){
            bulan = "8";
        }
        else if(bul.equalsIgnoreCase("September")){
            bulan = "9";
        }
        else if(bul.equalsIgnoreCase("Oktober")){
            bulan = "10";
        }
        else if(bul.equalsIgnoreCase("November")){
            bulan = "11";
        }
        else if(bul.equalsIgnoreCase("Desember")){
            bulan = "12";
        }
        else{
            bulan = null;
        }
        tahun = (String) thn.getSelectedItem();
        if(laki.isSelected()){
            jk = "l";
        }
        else {
            jk = "p";
        }
        if(namdep.getText().isEmpty() || nambel.getText().isEmpty() || nohp.getText().isEmpty() || username.getText().isEmpty() || password.getText().isEmpty() || tanggal.isEmpty() || bulan.isEmpty() || tahun.isEmpty() || jk.isEmpty()){
            JOptionPane.showMessageDialog(null,"Ada data yang kosong");
        }
        else {
            try{
                mod.setNamdep(namdep.getText());
                mod.setNambel(nambel.getText());
                mod.setNo_hp(nohp.getText());
                mod.setUsername(username.getText());
                mod.setPassword(password.getText());
                mod.setTanggal(tanggal);
                mod.setBulan(bulan);
                mod.setTahun(tahun);
                mod.setJ_kelamin(jk);
                crud.editAnggota(mod, user);
                JOptionPane.showMessageDialog(null,"Data Berhasil Diubah");
                Profil prof = new Profil(mod.getUsername());
                prof.setVisible(true);
            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(null,"Data gagal di input");
            }
        }
    }//GEN-LAST:event_updateActionPerformed

